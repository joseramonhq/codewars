package codewars.kyu8.multiply;

public class Multiply {
    /**
     * The multiply function multiplies two numbers and returns the result.
     *
     * @param a Pass the first value to be multiplied
     * @param b Pass the second value to be multiplied
     * @return A double
     * @author joseramonhq@gmail.com
     */
    public static Double multiply(Double a, Double b) {
        return a * b;
    }
}

